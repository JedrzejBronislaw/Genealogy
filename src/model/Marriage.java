package model;

public class Marriage {
			
	private String data;
	private String miejsce;
	private Person maz, zona;
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getMiejsce() {
		return miejsce;
	}
	public void setMiejsce(String miejsce) {
		this.miejsce = miejsce;
	}
	public Person getMaz() {
		return maz;
	}
	public void setMaz(Person maz) {
		this.maz = maz;
	}
	public Person getZona() {
		return zona;
	}
	public void setZona(Person zona) {
		this.zona = zona;
	}
	
	
}
