package dane;

public class Malzenstwo {
			
	private String data;
	private String miejsce;
	private Osoba maz, zona;
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getMiejsce() {
		return miejsce;
	}
	public void setMiejsce(String miejsce) {
		this.miejsce = miejsce;
	}
	public Osoba getMaz() {
		return maz;
	}
	public void setMaz(Osoba maz) {
		this.maz = maz;
	}
	public Osoba getZona() {
		return zona;
	}
	public void setZona(Osoba zona) {
		this.zona = zona;
	}
	
	
}
